// NOTE: When flashing using the STC_ISR tool, make sure to select "Use external clock"!!
// The default is always "Use internal RC clock", which will screw up the baud rate!!
void serial_open(void)
{
	AUXR |= (1<<6);     // 设置定时器1为1T模式
	TMOD = 0x20;				// 定时器T1使用工作方式2,8位自动重载
	SCON = 0x50;				// SM0=0 SM1=1 SM2=0 REN=1 TB8=0 RB8=0 TI=0 RI=0
	TH1 = 0xFD;					// 9600	(外接11.0592M晶振)
	TL1 = 0xFD;
	PCON = 0x80;				// SMOD =1    倍频
	TR1 = 1;					// 开始计时
}
	  
bit WaitComm(uchar *len)
{	 
	uint i=0;
	uchar j=0;
		
	if(RI)
    {
		rece_buf[j++]=SBUF;	  //接收到的数据放入缓冲区recebuf中
		RI=0;
		while(i<500) 
		{
		  	if(RI)
			{
			   	rece_buf[j++]=SBUF;
				RI = 0;
				i=0;
				if (j >= 32) break;	
			}
			i++;
		}
		*len = j;		
		return 0;		  //有数据接收到，返回0
	}
	else
	{
		return 1;		  //无数据接收到，返回1
	}
}



